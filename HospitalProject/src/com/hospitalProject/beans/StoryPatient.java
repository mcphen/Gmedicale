package com.hospitalProject.beans;


public class StoryPatient {

	private String namePatient;
	private String prenomPatient;
	private String numConsultation;
	private String numVisite;
	private String dateDebut;
	private String dateVisite;
	private int ordre;
	private String status;
	
	
	public String getNamePatient() {
		return namePatient;
	}
	public void setNamePatient(String namePatient) {
		this.namePatient = namePatient;
	}
	public String getPrenomPatient() {
		return prenomPatient;
	}
	public void setPrenomPatient(String prenomPatient) {
		this.prenomPatient = prenomPatient;
	}
	public String getNumConsultation() {
		return numConsultation;
	}
	public void setNumConsultation(String numConsultation) {
		this.numConsultation = numConsultation;
	}
	public String getNumVisite() {
		return numVisite;
	}
	public void setNumVisite(String numVisite) {
		this.numVisite = numVisite;
	}
	public String getDateDebut() {
		return dateDebut;
	}
	public void setDateDebut(String dateDebut) {
		this.dateDebut = dateDebut;
	}
	public String getDateVisite() {
		return dateVisite;
	}
	public void setDateVisite(String dateVisite) {
		this.dateVisite = dateVisite;
	}
	public int getOrdre() {
		return ordre;
	}
	public void setOrdre(int ordre) {
		this.ordre = ordre;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
