package com.hospitalProject.beans;

public class Visites {

	private int idVisites;
	private String numVisites;
	private String dateVisite;
	private String dateRv;
	private int ordre;
	private String symptome;
	private String statut;
	private String ordonnance;
	private int idConsultation;
	public int getIdVisites() {
		return idVisites;
	}
	public void setIdVisites(int idVisites) {
		this.idVisites = idVisites;
	}
	public String getNumVisites() {
		return numVisites;
	}
	public void setNumVisites(String numVisites) {
		this.numVisites = numVisites;
	}
	public String getDateVisite() {
		return dateVisite;
	}
	public void setDateVisite(String dateVisite) {
		this.dateVisite = dateVisite;
	}
	public String getDateRv() {
		return dateRv;
	}
	public void setDateRv(String dateRv) {
		this.dateRv = dateRv;
	}
	public int getOrdre() {
		return ordre;
	}
	public void setOrdre(int ordre) {
		this.ordre = ordre;
	}
	public String getSymptome() {
		return symptome;
	}
	public void setSymptome(String symptome) {
		this.symptome = symptome;
	}
	public String getStatut() {
		return statut;
	}
	public void setStatut(String statut) {
		this.statut = statut;
	}
	public String getOrdonnance() {
		return ordonnance;
	}
	public void setOrdonnance(String ordonnance) {
		this.ordonnance = ordonnance;
	}
	public int getIdConsultation() {
		return idConsultation;
	}
	public void setIdConsultation(int idConsultation) {
		this.idConsultation = idConsultation;
	}
}
